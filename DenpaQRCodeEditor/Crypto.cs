        private static byte GetMostSignificantNibble(byte b0, byte b1, bool encrypt=false)
        {

            int repeat = HighNibbleTable[b1 & 0x7F].Length;
            int multiplier = b0 / repeat;
            if(encrypt)
                return (byte)((HighNibbleTable[b1 & 0x7F][b0 % repeat] & 0xF0) + (multiplier * repeat));
            else
                return (byte)(((HighNibbleTable[b1 & 0x7F][b0 % repeat] & 0x0F) << 4) + (multiplier * repeat));
        }

        private static byte GetLeastSignificantNibble(byte b0, byte b1, bool encrypt = false)
        {
            var evenColumn = b0 % 2 == 0;
            var lineIndex = b1 % 8;
            byte startVal = (new byte[2][] { 
                    new byte[] { 0x00, 0x31, 0x4C, 0xF9, 0x00, 0xB9, 0xC4, 0xF1 },
                    new byte[] { 0x11, 0x0E, 0x5D, 0x8A, 0x11, 0x8E, 0xD5, 0x02 } 
                })[b0 % 2][lineIndex];
            if (encrypt)
                startVal >>= 4;
            else
                startVal &= 0xF;
 
            if ((lineIndex != 3 && lineIndex != 5) || (evenColumn ^ encrypt))
            {
                return (byte)((startVal + (evenColumn ? b0 : b0 - 1)) % 0x10);
            }
            return (byte)((startVal + (evenColumn ? b0 : b0 - 1) * 5) % 0x10);
        }

        public static byte Decrypt(byte b0, byte b1)
        {
            return (byte)(GetMostSignificantNibble(b0, b1) | GetLeastSignificantNibble(b0, b1));
        }

        public static Byte[] Decrypt(Byte[] bytearray)
        {
            if (bytearray == null)
                return null;
            byte[] result = new byte[bytearray.Length / 2];
            for (int i = 0; i < result.Length; i++)
            {
                result[i] = Decrypt(bytearray[(i*2)], bytearray[(i*2)+1]);
            }
            return result;
        }

        public static byte Encrypt(byte b0, byte b1)
        {
            return (byte)(GetLeastSignificantNibble(b0, b1, true) | GetMostSignificantNibble(b0, b1, true));
        }

        public enum EncryptType
        {
            ClearText = 0,
            Sequential,
            AlphaNumeric,
            Random
        };

        public static Byte[] Encrypt(Byte[] bytearray, EncryptType type = EncryptType.AlphaNumeric)
        {
            if (bytearray == null)
                return null;
            byte[] result = new byte[bytearray.Length * 2];
            for (int i = 0; i < bytearray.Length; i++)
            {
                switch (type)
                {
                    case EncryptType.AlphaNumeric:
                        result[(i * 2)] = AlphaNumericTable[bytearray[i]][0];
                        result[(i * 2) + 1] = AlphaNumericTable[bytearray[i]][1];
                        break;
                    case EncryptType.ClearText:
                        result[(i * 2)] = bytearray[i];
                        result[(i * 2) + 1] = 0;
                        break;
                    case EncryptType.Random:
                        Random random = new Random();
                        Byte j = (Byte)random.Next(0, 255);
                        result[(i * 2)] = Encrypt(bytearray[i], j);
                        result[(i * 2) + 1] = j;
                        break;
                    case EncryptType.Sequential:
                        result[(i * 2)] = Encrypt(bytearray[i], (byte)i);
                        result[(i * 2) + 1] = (byte)i;
                        break;
                }
            }
            return result;
        }
